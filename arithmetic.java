public class arithmetic {

    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        int sum = a + b;
        int diff = a - b;
        int mul = a * b;
        int mod = a % b;
        int div = a / b;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(mul);
        System.out.println(mod);
        System.out.println(div);


    }
}
